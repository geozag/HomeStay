  final String _poppinsRegular = 'assets/fonts/Poppins-Regular.ttf';

  // getter method
  String get poppinsLight => _poppinsLight;
  String get poppinsMedium => _poppinsMedium;
  String get poppinsRegular => _poppinsRegular;
}
